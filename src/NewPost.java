    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       int userId = this.userId;
    String entryDate = jTextField2.getText(); // Get the date in "dd-MM-yy" format
    String entryText = jTextArea1.getText();

    // Validate the input (you can add more specific validation)
    if (entryDate.isEmpty() || entryText.isEmpty()) {
        // Display an error message using JOptionPane
        JOptionPane.showMessageDialog(this, "Please enter both date and entry text.", "Entry Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if validation fails
    }

    // Convert the date to the "yy-MM-dd" format
    SimpleDateFormat inputFormat = new SimpleDateFormat("dd-MM-yy");
    SimpleDateFormat outputFormat = new SimpleDateFormat("yy-MM-dd");

    try {
        java.util.Date inputDate = inputFormat.parse(entryDate);
        java.util.Date currentDate = new java.util.Date();
        
        // Check if the input date is greater than the current date
        if (inputDate.after(currentDate)) {
            JOptionPane.showMessageDialog(this, "Birthday date cannot be in the future.", "Date Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method if the date is in the future
        }
        
        entryDate = outputFormat.format(inputDate);
    } catch (Exception e) {
        e.printStackTrace();
        // Handle the date parsing error
        JOptionPane.showMessageDialog(this, "Invalid date format. Please use 'dd-MM-yy'.", "Date Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if date parsing fails
    }

    // Database connection and query
    Connection conn = null;
    try {
        // Establish a database connection (modify the connection details as needed)
        String jdbcURL = "jdbc:mysql://localhost:3306/birthdays";
        String dbUser = "root";
        String dbPassword = "1234";
        conn = DriverManager.getConnection(jdbcURL, dbUser, dbPassword);

        // Create a SQL query to insert the entry into the database
        String sql = "INSERT INTO entries (user_id, name, date) VALUES (?, ?, ?)";
        PreparedStatement statement = conn.prepareStatement(sql);
        statement.setInt(1, userId);
        statement.setString(2, entryText);
        statement.setString(3, entryDate);

        int rowsInserted = statement.executeUpdate();

        if (rowsInserted > 0) {
            // Entry added successfully
            JOptionPane.showMessageDialog(this, "Birthday added successfully!");

            home homeFrame = new home(userId);
            homeFrame.initializeAndShow();
            dispose();
        } else {
            // Entry could not be added
            JOptionPane.showMessageDialog(this, "Failed to add the Birthday. Please try again.", "Birthday Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e, "Entry Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if (conn != null) {
                conn.close();
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    
